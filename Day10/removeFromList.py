thislist=["apple","banana","cherry"]
thislist.remove("banana")
print(thislist)

#removing first occurance of banana
thislist=["apple","banana","cherry","banana","kiwi"]
thislist.remove("banana")
print(thislist)

#pop()
thislist=["apple","banana","cherry"]
thislist.pop(1)
print(thislist)

#removing last item
thislist=["apple","banana","cherry"]
thislist.pop()
print(thislist)

#del keyword
thislist=["apple","banana","cherry"]
del thislist[0]
print(thislist)

#deleting entire list
thislist=["apple","banana","cherry"]
del thislist


#clearing list
thislist=["apple","banana","cherry"]
thislist.clear()
print(thislist)




